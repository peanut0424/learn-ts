// 基本數據類型
// 1.數值類型

let len:number = 10

let score:number = 85.5

console.log(typeof len);
console.log(score);


// 2.字符串類型
let str:string = '糖葫蘆'
console.log(str);

// 3.布爾類型

let isPrime:boolean = false
console.log(isPrime);

// 4.undefined
// 聲明變量但是不賦值就是undefined
let abc:string
console.log(abc); // undefined

// 5.null
// 聲明並賦值為null
let obj:null = null
console.log(obj);
